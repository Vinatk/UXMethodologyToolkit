                   About Us
                 </h1>
                 <div class="col-md-12 mb-5">
               <p>The UX Methodology Toolkit app was developed as part of a final MSc project deliverables.
                 Understanding the importance of UX research and the impact of UX methods in gathering the right user data
                can determine the success of any product, application, or services. Therefore, the objective of this
                 toolkit is to help suggest users with the UX method that will be best suited for wide variety development
                  and UX research projects based on the input provided by the user. The methods suggested by the toolkit
                  are accumulated from academic research and survey response by UX experts, UI/UX communities,
                  and design enthusiast in general. We hope this toolkit will be more or less beneficial to
                  the user’s own development project and being able to carry out these methods with the supporting
                  material and resources provided.</p>
                 </div>

